var day = "sunday";
var result = "";

switch (day) {
    case 1:
        result = "monday";
        console.log(result);
        break;

    case 2:
        result = "tuesday";
        console.log(result);
        break;

    case 3:
        result = "wednesday";
        console.log(result);
        break;

    case 4:
        result = "thursday";
        console.log(result);
        break;

    case 5:
        result = "friday";
        console.log(result);
        break;

    case 6:
        result = "saturday";
        console.log(result);
        break;

    case 7:
        result = "sunday";
        console.log(result);
        break;
    
    default:
      console.log("Not a day")
}
